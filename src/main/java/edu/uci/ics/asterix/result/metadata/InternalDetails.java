package edu.uci.ics.asterix.result.metadata;

import com.fasterxml.jackson.annotation.JsonProperty;

import java.util.List;

public class InternalDetails {

    @JsonProperty("FileStructure")
    private String fileStructure;

    @JsonProperty("Autogenerated")
    private Boolean autogenerated;

    @JsonProperty("PartitioningStrategy")
    private String partitioningStrategy;

    @JsonProperty("PartitioningKey")
    private List<List<String>> PartitioningKey;

    @JsonProperty("PrimaryKey")
    private List<List<String>> primaryKey;

    public Boolean getAutogenerated() {
        return autogenerated;
    }

    public void setAutogenerated(Boolean autogenerated) {
        this.autogenerated = autogenerated;
    }

    public List<List<String>> getPrimaryKey() {
        return primaryKey;
    }

    public void setPrimaryKey(List<List<String>> primaryKey) {
        this.primaryKey = primaryKey;
    }

    public String getFileStructure() {
        return fileStructure;
    }

    public void setFileStructure(String fileStructure) {
        this.fileStructure = fileStructure;
    }

    public String getPartitioningStrategy() {
        return partitioningStrategy;
    }

    public void setPartitioningStrategy(String partitioningStrategy) {
        this.partitioningStrategy = partitioningStrategy;
    }

    public List<List<String>> getPartitioningKey() {
        return PartitioningKey;
    }

    public void setPartitioningKey(List<List<String>> partitioningKey) {
        PartitioningKey = partitioningKey;
    }
}
